//Deverá receber dois parametros e retornar o seu valor


function somar(n1:number, n2:number):number{
  return n1 + n2
}

function subtrair(n3:number, n4:number):number{
  return n3 - n4
}

function multiplicar(n5:number, n6:number):number{
  return n5 * n6
}

function dividir(n7:number, n8:number):number{
  return n7 / n8
}

export{somar, subtrair, multiplicar, dividir}

